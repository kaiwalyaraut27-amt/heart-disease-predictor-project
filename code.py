import joblib, json, logging
from datetime import datetime
import pandas as pd

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger("HeartPredictor")

class HeartDiseasePredictor:
    """Production heart disease risk predictor."""

    VALID_RANGES = {
        "age":      (18, 120),
        "trestbps": (60, 250),
        "chol":     (50, 600),
        "thalach":  (50, 250),
        "oldpeak":  (0, 10)
    }

    def __init__(self, model_path, metadata_path):
        self.model    = joblib.load(model_path)
        with open(metadata_path) as f:
            self.meta = json.load(f)
        self.features = self.meta["features"]
        logger.info(f"Loaded. Test AUC: {self.meta['metrics']['test_auc']}")

    def _validate(self, patient):
        for field, (lo, hi) in self.VALID_RANGES.items():
            if field in patient:
                val = patient[field]
                if not (lo <= val <= hi):
                    raise ValueError(f"{field}={val} out of range [{lo},{hi}]")

    def predict(self, patient_dict):
        try:
            self._validate(patient_dict)
            df    = pd.DataFrame([patient_dict])[self.features]
            pred  = self.model.predict(df)[0]
            proba = self.model.predict_proba(df)[0][1]
            risk  = "High" if proba > 0.7 else "Moderate" if proba > 0.4 else "Low"
            result = {
                "prediction":  int(pred),
               "label": "Heart Disease Detected" if pred == 1 else "No Disease",
                "probability": round(float(proba), 4),
                "risk_level":  risk,
                "timestamp":   datetime.now().isoformat()
            }
            logger.info(f"Prediction: {result['label']} (p={proba:.2%}, risk={risk})")
            return result
        except Exception as e:
            logger.error(f"Error: {e}")
            return {"error": str(e)}

    def batch_predict(self, patients):
      results = []
      for patient in patients:
          result = self.predict(patient)
          results.append(result)
      return results

