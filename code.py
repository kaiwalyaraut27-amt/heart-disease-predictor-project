# Data Cleaning

import pandas as pd
import numpy as np

df = pd.read_csv("/content/heart.csv")
print("\nMissing Values Before Cleaning:")
print(df.isnull().sum())
print("  ")

print(f"Original shape: {df.shape}")
print("  ")

# Check for duplicates
print(f"Duplicates: {df.duplicated().sum()}")
df = df.drop_duplicates()
print("  ")

# Handle missing values
# Some versions use "?" for missing — replace with NaN:
df = df.replace("?", np.nan)

# Convert columns to numeric (in case "?" left them as object):
for col in df.columns:
    df[col] = pd.to_numeric(df[col], errors="coerce")

print("Missing after conversion:")
print(df.isnull().sum())
print("  ")

# ── Handle impossible 0 values ──
# Cholesterol of 0 is physiologically impossible:
print(f"Zero cholesterol: {(df['chol'] == 0).sum()}")
df["chol"] = df["chol"].replace(0, np.nan)
print("  ")

# Resting BP of 0 is also impossible:
print(f"Zero trestbps: {(df['trestbps'] == 0).sum()}")
df["trestbps"] = df["trestbps"].replace(0, np.nan)
print("  ")

# ── Fill remaining NaN with median ──
numeric_cols = df.select_dtypes(include="number").columns
df[numeric_cols] = df[numeric_cols].fillna(df[numeric_cols].median())

#Checking ca column
print("\nValue Counts for ca:")
print(df["ca"].value_counts())

# Replace invalid value 4 with NaN
df["ca"] = df["ca"].replace(4, np.nan)

# Fill missing values with median
df["ca"] = df["ca"].fillna(df["ca"].median())

# checking thal column
print("\nValue Counts for thal:")
print(df["thal"].value_counts())

# Replace invalid value 4 with NaN
df["thal"] = df["thal"].replace(4, np.nan)

# Fill missing values with median
df["thal"] = df["thal"].fillna(df["thal"].median())


# =========================================
# OUTLIER CHECK — chol
# =========================================

Q1 = df["chol"].quantile(0.25)
Q3 = df["chol"].quantile(0.75)

IQR = Q3 - Q1

lower = Q1 - 1.5 * IQR
upper = Q3 + 1.5 * IQR

# Find outliers
chol_outliers = df[
    (df["chol"] < lower) |
    (df["chol"] > upper)
]

print("\nChol Outliers Found:")
print(len(chol_outliers))

print(f"Lower Bound: {lower:.2f}")
print(f"Upper Bound: {upper:.2f}")

# CAP OUTLIERS — chol

df["chol"] = df["chol"].clip(
    lower=lower,
    upper=upper
)

# CAP OUTLIERS — trestbps

Q1_bp = df["trestbps"].quantile(0.25)
Q3_bp = df["trestbps"].quantile(0.75)

IQR_bp = Q3_bp - Q1_bp

lower_bp = Q1_bp - 1.5 * IQR_bp
upper_bp = Q3_bp + 1.5 * IQR_bp

df["trestbps"] = df["trestbps"].clip(
    lower=lower_bp,
    upper=upper_bp
)
print("  ")
print("Missing after cleaning:")
print(df.isnull().sum())
print("  ")

print("\nTotal Missing Values:")
print(df.isnull().sum().sum())
print("  ")

print(f"Final shape: {df.shape}")

df.to_csv("heart_clean.csv", index=False)
